package com.example.capstone.bean;

public class GroupFreeBeanElement {
    private String start, end;
    public GroupFreeBeanElement(String start, String end) {
        this.start = start;
        this.end = end;
    }

    public void setStart(String start) {
        this.start = start;
    }

    public String getStart() {
        return start;
    }

    public void setEnd(String end) {
        this.end = end;
    }

    public String getEnd() {
        return end;
    }
}
